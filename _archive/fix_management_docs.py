    text = p.read_text(encoding="utf-8")
    for old, new in pairs:
        if old not in text:
            print(f"  [未命中] {path}: {old[:50]}...")
            continue
        text = text.replace(old, new, 1)
    p.write_text(text, encoding="utf-8")
    print(f"[完成] {path}")


